import java.util.*;
public class bit_manipulation {
    public static void main(String args[]) {
        System.out.println(5&6);//&& is for comparision(and operator)
        System.out.println(5|6);//|=pipe(or operator)
        System.out.println(5^6);//XOR operator
        System.out.println(~5);//1's complement
        System.out.println(5<<2);//left shift
        System.out.println(6>>1);//right shift
    }
} 